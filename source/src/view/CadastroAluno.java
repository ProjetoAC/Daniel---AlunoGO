    public void cadastrar_Aluno() {
        if (txtRua.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Rua Invalido");
            txtRua.grabFocus();
            return;
        } else if (txtBairro.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Bairro Invalido");
            txtBairro.grabFocus();
            return;
        } else if (txtCidade.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Cidade Invalido");
            txtCidade.grabFocus();
            return;
        } else if (txtIdade.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Idade Invalido");
            txtIdade.grabFocus();
            return;
        } else if (txtNome_Responsavel.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Nome Responsavel Invalido");
            txtNumero.grabFocus();
            return;
        } else if (txtTelefone_Responsavel.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Telefone Responsavel Invalido");
            txtTelefone_Responsavel.grabFocus();
            return;
        } else if (txtParentesco_Responsavel.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Parentesco Responsavel Invalido");
            txtParentesco_Responsavel.grabFocus();
            return;
        } else if (txtNome_completo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Nome Completo Invalido");
            txtNome_completo.grabFocus();
            return;
        } else if (txtComplemento.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Complemento Invalido");
            txtComplemento.grabFocus();
            return;
        } else if (txtAtivo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Ativo Invalido");
            txtAtivo.grabFocus();
            return;
        }

        if ((aluno == null) || (alunocontroller == null)) {
            aluno = new Aluno();
            alunocontroller = new AlunoController();
        }
        aluno.setAtivo(txtAtivo.getText());
        aluno.setNome_completo(txtNome_completo.getText());
        aluno.setNascimento(txtIdade.getText());
        aluno.setNis(Integer.parseInt(txtNis.getText()));
        aluno.setObs_aluno(txtObsAluno.getText());
        aluno.setInstituicaoid(Integer.parseInt(txtInstituicao.getText()));
        aluno.setRua(txtRua.getText());
        aluno.setNumero(Integer.parseInt(txtNumero.getText()));
        aluno.setBairro(txtBairro.getText());
        aluno.setCidade(txtCidade.getText());
        aluno.setComplemento(txtComplemento.getText());
        aluno.setEncaminhado(txtEncaminhado.getText());
        aluno.setNome_responsavel(txtNome_Responsavel.getText());
        aluno.setTelefone_responsavel(txtTelefone_Responsavel.getText());
        aluno.setParentesco_responsavel(txtParentesco_Responsavel.getText());
        aluno.setWhats_responsavel(txtWhats.getText());
        aluno.setFacebook_responsavel(txtFacebook.getText());
        aluno.setEmail_responsavel(txtEmail.getText());
        aluno.setObs_responsavel(txtObsResponsavel.getText());

        if (alunocontroller.insereAluno(aluno)) {
        }
    }

